function [result] = DH_Utils_NormalizeTrainingData(...
    data, cutIdRange, radiusRange, hardnessRange, targetRange)
%
%
%%%%%%%%%%%%%%%%%5

    constantsHolder = DH_TrainingItemConstants();

    result = zeros(size(data));
    
    result(1:end, constantsHolder.cutIdIndex) =      ...
        DH_Utils_NormalizeData(                      ...
            data(1:end, constantsHolder.cutIdIndex), ...
            cutIdRange.low, cutIdRange.up          , ...
            targetRange.low, targetRange.up        );
    
            
    result(1:end, constantsHolder.radiusIndex) =      ...
        DH_Utils_NormalizeData(                       ...
            data(1:end, constantsHolder.radiusIndex), ...
            radiusRange.low , radiusRange.up        , ...
            targetRange.low, targetRange.up         );

    result(1:end, constantsHolder.hardnessIndex) =      ...
        DH_Utils_NormalizeData(                         ...
            data(1:end, constantsHolder.hardnessIndex), ...
            hardnessRange.low, hardnessRange.up       , ...
            targetRange.low  , targetRange.up         );
            
return
end


